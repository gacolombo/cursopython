"""
Tipos de dados
str - string - textos 'Assim' "Assim"
int - inteiro - 12345 -10 -20 -50 -60 -15000 15000
float - real/ponto flutuante - 0.2 1.50 -50.20 -502.30
bool - booleano/logico - True/False 10 == 10
"""
# Str: nome
print('Gabriel Colombo Araujo', type('Gabriel Colombo Araujo'))

# Int: Idade
print(24, type(24))

# Float: Altura
print(1.81, type(1.81))

# Bool: Maior de idade?
print(24 >= 18, type(24 > 18))