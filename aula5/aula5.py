"""
Os operadores aritmeticos tem comportamentos diferentes dependendo dos tipos de dados:

+: adicao ou concatenacao(str + str)
-: subtracao
*: multiplicacao ou repeticao(int * str)
/: divisao
//: divisao inteira
**: potencia
%: resto da divisao
(): mudar a precedencia dos operadores aritmeticos
"""

print('Adicao 10 + 10 = ', 10 + 10, 'ou concatenacao "Gabriel " + "Colombo": ', 'Gabriel ' + 'Colombo')
print('Subtracao 10 - 10 = ', 10 - 10)
print('Multiplicacao 10 * 10 = ', 10 * 10, ' ou repeticao "10" * 10: ', 10 * '10')
print('Divisao 10 / 8 =', 10 / 8)
print('Divisao inteira 10 // 8 =', 10 // 8)
print('Resto da divisao 10 % 8 =', 10 % 8)
print('Potenciacao 10 ** 2 =', 10 ** 2)
